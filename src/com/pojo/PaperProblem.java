package com.pojo;

public class PaperProblem {
	private String qpname;
	private int qid;
	private int qtype;
	private String qque;
	private String qcopt;
	private String qans;
	private String qscore;
	public String getQpname() {
		return qpname;
	}
	public void setQpname(String qpname) {
		this.qpname = qpname;
	}
	public int getQid() {
		return qid;
	}
	public void setQid(int qid) {
		this.qid = qid;
	}
	public int getQtype() {
		return qtype;
	}
	public void setQtype(int qtype) {
		this.qtype = qtype;
	}
	public String getQque() {
		return qque;
	}
	public void setQque(String qque) {
		this.qque = qque;
	}
	public String getQcopt() {
		return qcopt;
	}
	public void setQcopt(String qcopt) {
		this.qcopt = qcopt;
	}
	public String getQans() {
		return qans;
	}
	public void setQans(String qans) {
		this.qans = qans;
	}
	public String getQscore() {
		return qscore;
	}
	public void setQscore(String qscore) {
		this.qscore = qscore;
	}
}
